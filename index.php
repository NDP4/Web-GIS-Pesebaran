<script type="text/javascript">
    // disable
    function disableButton() {
        $("#cek_nik_process").prop('disabled', true);
    }
    // enable
    function enableButton() {
        $("#cek_nik_process").prop('disabled', false);
    }
    // inputan khusus angka
    function angka(evt) {
        var charCode = (evt.which) ? evt.which : event.keyCode
        if (charCode > 31 && (charCode < 48 || charCode > 57))
            return false;
        return true;
    }
    //
    function showModals(id, nama) {
        $(".modal-body").text("");
        var url_all = "https://bantulkab.go.id/beranda/list/" + id + ".html";
        $.ajax({
            url: url_all,
            success: function(data) {
                $(".modal-body").html(data);
            }
        });
        // $('.modal-title').text(nama);
        $('#myModal3').modal('show');
    }
    //
    jQuery(document).ready(function() {
        // ajax get data kontak kami
        $(document).on('click', '.aplikasi_bantul', function() {
            $(".data_looping").empty();
            // form data         
            var formData = new FormData();
            var link_gambar = array();
            formData.append('id_aplikasi', $(this).attr('id_aplikasi'));
            // proses
            $.ajax({
                url: 'https://bantulkab.go.id/beranda/ajax_get_data_aplikasi.html',
                dataType: 'json',
                type: 'POST',
                success: function(data) {
                    if (data.status === 'success') {
                        //loop data
                        if (data.html.length == 0) {
                            // kalau kosong
                            $(".data_looping").append('Data tidak ditemukan !');
                        } else {
                            //looping isi json 
                            for (var i = 0; i < data.html.length; i++) {
                                link_gambar[i] = data.html[i].img_path + '' + data.html[i].img_gambar;
                                if (data.html[i].link == '') {
                                    // reloaddata
                                    $(".data_looping").append(
                                        '<li class="text-center"><small><a href="#" class="aplikasi_bantul" id_aplikasi="' + data.html[i].id_aplikasi + '"><img src="' + link_gambar + '" alt="Clients" title="' + data.html[i].singkatan_aplikasi + '"></a>' + data.html[i].nama_aplikasi + '</small></li>'
                                    );
                                } else {
                                    // reloaddata
                                    $(".data_looping").append(
                                        '<li class="text-center"><small><a href="' + data.html[i].link + '" target="_blank"><img src="' + link_gambar + '" alt="Clients" title="' + data.html[i].singkatan_aplikasi + '"></a>' + data.html[i].nama_aplikasi + '</small></li>'
                                    );
                                }

                            }
                        }
                        // open dialog
                        $('.form-edit').modal({
                            backdrop: "static",
                            keyboard: true,
                            show: true
                        });
                    } else {
                        // notification
                        swal("ERROR", data.html, "warning");
                    }
                },
                error: function() {
                    // notification
                    swal("ERROR", "Maaf, data gagal ditampilkan !", "warning");
                },
                data: formData,
                cache: false,
                contentType: false,
                processData: false
            }, 'json');
            // false
            return false;
        });
        //
        var form = this;
        $('.form-ajax').each(function(index) {
            $(form).submit(function(e) {
                // Google reCaptcha
                if ((typeof grecaptcha !== 'undefined') && $(this).find(".g-recaptcha").length) {
                    if (grecaptcha.getResponse().length === 0) {
                        e.preventDefault;
                        return false;
                    }
                }
            });
        });
        // proses cek nik
        $(document).on('click', '#cek_nik_process', function(event) {
            // event
            event.preventDefault();
            // form
            var form = $(this).closest('form');
            var formData = new FormData();
            formData.append('nik', form.find('input[name="nik"]').val());
            formData.append('no_kk', form.find('input[name="no_kk"]').val());
            formData.append('g-recaptcha-response', form.find('textarea[name="g-recaptcha-response"]').val());
            // proses
            $.ajax({
                url: 'https://bantulkab.go.id/beranda/cek_nik.html',
                dataType: 'json',
                type: 'POST',
                success: function(data) {
                    if (data.status === 'success') {
                        // notifikasi
                        $('#notif_nik').text(data.html);
                        $('#pesan').removeClass('style-msg infomsg').addClass('style-msg successmsg');
                        disableButton();
                    } else {
                        // notifikasi
                        $('#notif_nik').text(data.html);
                        $('#pesan').removeClass('style-msg infomsg').addClass('style-msg errormsg');
                        disableButton();
                    }
                },
                error: function(data) {
                    $('#notif_nik').text(data.html);
                    $('#pesan').removeClass('style-msg infomsg').addClass('style-msg alertmsg');
                    disableButton();
                },
                data: formData,
                cache: false,
                contentType: false,
                processData: false
            }, 'json');
        });
        // reset cek nik
        $(document).on('click', '#reset_nik_process', function() {
            // default pesan
            $('#notif_nik').text('SILAHKAN MENGISI DATA DIBAWAH INI!');
            $('#pesan').addClass('style-msg infomsg');
            grecaptcha.reset();
            enableButton();
        });
    });
</script>